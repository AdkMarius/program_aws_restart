print("Python has three numeric types: int, float, and complex")

# Int type
myValue = 1
print(myValue)
print(type(myValue))

print(str(myValue) + " is of the data type " + str(type(myValue)))

# Float type
myValue=3.14
print(myValue)
print(type(myValue))

print(str(myValue) + " is of the data type " + str(type(myValue)))

# Complex type
myValue=5j
print(myValue)
print(type(myValue))

print(str(myValue) + " is of the data type " + str(type(myValue)))

# Boolean type
myValue=True
print(myValue)
print(type(myValue))

print(str(myValue) + " is of the data type " + str(type(myValue)))